#Single Inheritance
class father():

    key_F = '1BHK';       # Father Attribute

    def home(self):       # Father Method
        print('Father home')


class son():

    key_S = '2BHK'   # Son Attribute

    def home(self):     # Son Attribute
        print('Son home')

obj_F = father()
obj_S = son()

print(obj_F.home)
print(obj_F.key_F)





